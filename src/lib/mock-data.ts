const generatePriceHistory = (currentYes: number) => {
  const points = [];
  let price = currentYes - 0.15 + Math.random() * 0.1;
  for (let i = 30; i >= 0; i--) {
    price = Math.max(0.05, Math.min(0.95, price + (Math.random() - 0.48) * 0.04));
    points.push({
      time: new Date(Date.now() - i * 24 * 60 * 60 * 1000).toISOString(),
      yes: parseFloat(price.toFixed(2)),
      no: parseFloat((1 - price).toFixed(2)),
    });
  }
  points[points.length - 1].yes = currentYes;
  points[points.length - 1].no = parseFloat((1 - currentYes).toFixed(2));
  return points;
};

